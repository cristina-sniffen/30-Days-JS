// pre and post increment
let count = 0
console.log(count++) // 0
console.log(count)  // 1

count = 0
console.log(++count) // 1
console.log(count)  // 1

// pre and post deccrement
count = 0
console.log(count--) // 0
console.log(count)  // -1

count = 0
console.log(--count) // -1
console.log(count)  // -1